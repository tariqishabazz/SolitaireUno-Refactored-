    /// <summary>
    /// Specifies the possible card values in a standard deck of playing cards.
    /// </summary>
    /// <remarks>This enumeration defines values from Ace through King, corresponding to the ranks commonly
    /// used in card games. The underlying integer values start at 1 for Ace and increment by one for each subsequent
    /// rank.</remarks>
    public enum Values { Ace = 1, Two, Three, Four, Five, Six, Seven, Eight, Nine, Ten, Jack, Queen, King } 
}
